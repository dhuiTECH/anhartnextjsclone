    <li><strong>Baseboards:</strong> Where walls meet floors</li>
    <li><strong>Recessed lighting:</strong> Ceiling penetrations</li>
    <li><strong>Attic access:</strong> Poorly sealed hatches</li>
    <li><strong>Exhaust vents:</strong> Dryer and bathroom fan connections</li>
    <li><strong>Utility penetrations:</strong> Plumbing and wiring entry points</li>
