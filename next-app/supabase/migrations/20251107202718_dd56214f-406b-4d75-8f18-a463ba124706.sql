  <p><strong>Caulking:</strong> Seal stationary gaps with silicone or latex caulk ($5 per tube covers multiple areas)</p>
  
  <p><strong>Door sweeps:</strong> Install bottom door sweeps to block under-door drafts ($10-20, immediate impact)</p>
  
  <p><strong>Outlet gaskets:</strong> Add foam gaskets behind outlet covers on exterior walls ($10 for multi-pack)</p>
  
  <p><strong>Window film:</strong> Apply clear insulation film to create air pockets ($15-25 per kit, reduces window heat loss by 25-50%)</p>

