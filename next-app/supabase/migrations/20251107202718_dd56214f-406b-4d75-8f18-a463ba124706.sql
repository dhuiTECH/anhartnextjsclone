  <h2>2. Seal Air Leaks and Eliminate Drafts</h2>
  
  <p>Air leakage is among the most significant sources of energy waste in Canadian homes. Small gaps and cracks collectively can equal leaving a window wide open 24/7.</p>

  <h3>Common Air Leak Sources</h3>
  
  <ul>
