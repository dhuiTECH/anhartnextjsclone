  <h2>2. Seal Air Leaks and Eliminate Drafts</h2>
  
  <p>Air leakage is one of the most significant sources of energy waste in Canadian homes. The cumulative effect of small gaps and cracks around doors, windows, electrical outlets, and other openings can equal leaving a window open 24/7.</p>

  <h3>Common Sources of Air Leaks:</h3>
  
  <ul>
    <li><strong>Windows and doors:</strong> Gaps around frames, worn weatherstripping</li>
    <li><strong>Electrical outlets and switches:</strong> Especially on exterior walls</li>
    <li><strong>Baseboards and crown molding:</strong> Where walls meet floors and ceilings</li>
    <li><strong>Recessed lighting:</strong> Fixtures penetrating the ceiling</li>
    <li><strong>Attic hatches:</strong> Often poorly sealed access points</li>
    <li><strong>Dryer vents and exhaust fans:</strong> Connections to exterior</li>
    <li><strong>Plumbing and wiring penetrations:</strong> Where utilities enter the home</li>
  </ul>

  <h3>DIY Sealing Solutions:</h3>
  
  <p><strong>Weather-stripping:</strong> Apply self-adhesive foam or V-strip weatherstripping around doors and windows. Quality materials cost $3-10 per door and can save $15-30 monthly.</p>
  
  <p><strong>Caulking:</strong> Use silicone or acrylic latex caulk to seal stationary gaps around window and door frames, baseboards, and other openings. A $5 tube of caulk can seal dozens of leaks.</p>
  
  <p><strong>Door sweeps:</strong> Install bottom door sweeps to block drafts under exterior doors. These cost $10-20 and provide immediate results.</p>
  
  <p><strong>Outlet insulation:</strong> Install foam gaskets behind outlet and switch plate covers on exterior walls ($10 for a multi-pack).</p>
  
  <p><strong>Window film:</strong> Apply clear plastic window insulation film to create an insulating air pocket. Window insulation kits cost $15-25 and can reduce heat loss through windows by 25-50%.</p>

  <h3>Professional Energy Audit</h3>
  
  <p>Consider a professional home energy audit (often subsidized by provincial programs). Auditors use thermal imaging and blower door tests to identify hidden air leaks and insulation gaps. Many provinces offer rebates that cover 50-100% of audit costs.</p>

