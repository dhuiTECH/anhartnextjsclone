  <p><strong>Annual professional maintenance:</strong> Schedule a professional tune-up before winter ($100-200). Technicians clean burners, check safety systems, calibrate thermostats, and optimize efficiency. This preventive maintenance can prevent costly emergency repairs and extend system life by 5-10 years.</p>
  
  <p><strong>Clean vents and registers:</strong> Vacuum supply and return vents quarterly to ensure unobstructed airflow.</p>
  
  <p><strong>Check for duct leaks:</strong> Seal any visible duct leaks with metal-backed tape (not cloth duct tape). Duct leakage can waste 20-30% of heating energy.</p>

  <h3>When to Consider System Replacement:</h3>
  
